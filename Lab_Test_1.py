from __future__ import annotations


def factorial(n: int) -> int:
    """Return n! for non-negative integers."""
    if n < 0:
        raise ValueError("factorial is undefined for negative integers")
    result = 1
    for value in range(2, n + 1):
        result *= value
    return result


