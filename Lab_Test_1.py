def analyze_marks(student_marks):
    """Compute mean and list students above mean."""
    marks = list(student_marks.values())
    mean_val = sum(marks) / len(marks)

    above_mean = [name for name, mark in student_marks.items() if mark > mean_val]

    return mean_val, above_mean


